#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*dst;

	dst = s;
	i = 0;
	while (n > i)
	{
		dst[i] = '\0';
		i++;
	}
	s = (void *) dst;
}
